package com.mkpits.datatypes;


public class IntegralDatatypes {
 
public static void main(String[] args) {
		
	//1byte= 8bits
		//memory size=1 byte
	// default value=0
	    byte num=127;
		System.out.println(num);
		
		//memeory size=2 byte
        short data=32767;
		System.out.println(data);
		
		//memory size=4 byte
		int demo=2147483647;
		System.out.println(demo);
		
		//memory size= 8 byte
		long term=9223372036854775807l;
		System.out.println(term);
		
		
		

	}

}
