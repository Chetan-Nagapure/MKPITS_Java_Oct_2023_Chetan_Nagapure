//Syntax: -Operand

// JavaScript Unary negation(-) Operator while working with numbers.
const a = 10;
const b = -a;
console.log(b);//-10
console.log(typeof b);//number

//Unary negation(-) Operator working with non-numbers.
const x = "20";
const y = -x;
console.log(y);//-20
console.log(typeof y);//number


