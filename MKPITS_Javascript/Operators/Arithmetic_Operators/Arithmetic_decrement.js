//JavaScript decrement operator is used to decrease the value of the variable by one. 
//The value returned from the operand depends on whether the decrement operator was on the left(prefix decrement) or right(postfix decrement).


// If the operator is used before the operand then the value is decreased by one and then returned.
let x = 10;
console.log(x--);//10
console.log(x);//9

// If the operator is used after the operand then the value is first returned and then decremented.
let y = 10;
console.log(--y);//9
console.log(y);//9