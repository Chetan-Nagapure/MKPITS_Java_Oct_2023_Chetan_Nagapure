//if we want the multiplication of any two numbers then this operator can be useful. 
//a*b
//simple multiplication of two numbers.
let a = 10;
let b = 20;
let mul = (a * b);
console.log(mul);//200

//if we take one infinity value  and number, it will give us infinity. 
let c = Infinity;
let d = 10;
let mul1 = (c * d);
console.log(mul1);//Infinity

console.log(Infinity * 100);//infinity

//if we take one infinity value  and zero value, it will give us NaN.
console.log(Infinity * 0);//NaN

//if we take one NaN value  and number, it will give us NaN.
console.log(NaN * 100);//NaN