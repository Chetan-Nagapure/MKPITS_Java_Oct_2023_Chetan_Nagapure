//Multiplication Assignment Operator(*=) JavaScript is used to multiply two operands and assign the result to the right operand.

//we multiply two numerical values using the Multiplication Assignment Operator(*=) and assign the result variable in javascript.

let number = 10;
number *= 20;
console.log(number);//200

//Here we perform multiplication with a non-Numeric value and it will return NaN using the Multiplication Assignment Operator(*=) in javascript.
let number1 = 10;
number1 *= "Hello World";
console.log(number1);//NaN

   
