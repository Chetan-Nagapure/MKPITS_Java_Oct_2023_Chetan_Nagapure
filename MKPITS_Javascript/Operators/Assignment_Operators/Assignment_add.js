//JavaScript Addition assignment operator(+=) adds a value to a variable, The Addition Assignment (+ =) Sums up left and right operand values and then assigns the result to the left operand. 
//The two major operations that can be performed using this operator are the addition of numbers and the concatenation of strings.

//Syntax: a += b 

// When the operator is used on a number addition is performed and concatenation is performed on strings.

//we will concatenate two numbers using the Addition Assignment operator(+=).
//concatenation of two number 
let x = 2; 
let y = 5; 
console.log(x += y); //7

//we will concatenate two strings by using the Addition Assignment operator(+=).
//concatenation of two string 
let a = 'Hello';
let b = 'world';
console.log(a += b);//Helloworld 
